namespace Calculator
{
    public class Operations
    {
        public float Sum(float v1, float v2) {
            float result = v1 + v2;
            return result;
        }

        public float Subtract(float v1, float v2) {
            float result = v1 - v2;
            return result;
        }
        public float Divide(float v1, float v2) {
            float result = v1 / v2;
            return result;
        }
        public float Multiply(float v1, float v2) {
            float result = v1 * v2;
            return result;
        }
    }
}